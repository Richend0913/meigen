def generate_sitemap():
    lines = ['<?xml version="1.0" encoding="UTF-8"?>']
    lines.append('<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">')
    today = datetime.now().strftime("%Y-%m-%d")

    # Index
    lines.append(f'<url><loc>{SITE_URL}/index.html</loc><lastmod>{today}</lastmod><changefreq>weekly</changefreq><priority>1.0</priority></url>')

    # Category pages
    for tkey in QUOTE_TAGS:
        lines.append(f'<url><loc>{SITE_URL}/quotes/category-{tkey}.html</loc><lastmod>{today}</lastmod><changefreq>weekly</changefreq><priority>0.8</priority></url>')

    # Person pages
    for p in PEOPLE:
        lines.append(f'<url><loc>{SITE_URL}/quotes/{p["id"]}.html</loc><lastmod>{today}</lastmod><changefreq>monthly</changefreq><priority>0.7</priority></url>')

    lines.append('</urlset>')
    return '\n'.join(lines)


# ===== MAIN =====

def main():
    base_dir = os.path.dirname(os.path.abspath(__file__))
    quotes_dir = os.path.join(base_dir, "quotes")
    os.makedirs(quotes_dir, exist_ok=True)

    print(f"Generating site with {len(PEOPLE)} people...")

    # Generate person pages
    for person in PEOPLE:
        filepath = os.path.join(quotes_dir, f"{person['id']}.html")
        with open(filepath, "w", encoding="utf-8") as f:
            f.write(generate_person_page(person))
        print(f"  [PERSON] {person['name']} -> {filepath}")

    # Generate category pages
    for tkey, tinfo in QUOTE_TAGS.items():
        filepath = os.path.join(quotes_dir, f"category-{tkey}.html")
        with open(filepath, "w", encoding="utf-8") as f:
            f.write(generate_category_page(tkey, tinfo))
        print(f"  [CATEGORY] {tinfo['name']} -> {filepath}")

    # Generate index
    index_path = os.path.join(base_dir, "index.html")
    with open(index_path, "w", encoding="utf-8") as f:
        f.write(generate_index())
    print(f"  [INDEX] -> {index_path}")

    # Generate sitemap
    sitemap_path = os.path.join(base_dir, "sitemap.xml")
    with open(sitemap_path, "w", encoding="utf-8") as f:
        f.write(generate_sitemap())
    print(f"  [SITEMAP] -> {sitemap_path}")

    # Generate robots.txt
    robots_path = os.path.join(base_dir, "robots.txt")
    with open(robots_path, "w", encoding="utf-8") as f:
        f.write(f"User-agent: *\nAllow: /\nSitemap: {SITE_URL}/sitemap.xml\n")
    print(f"  [ROBOTS] -> {robots_path}")

    # Stats
    total_quotes = sum(len(p["quotes"]) for p in PEOPLE)
    print(f"\nDone! {len(PEOPLE)} people, {total_quotes} quotes, {len(QUOTE_TAGS)} categories.")


if __name__ == "__main__":
    main()
